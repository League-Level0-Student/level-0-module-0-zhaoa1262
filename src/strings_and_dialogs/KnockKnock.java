package strings_and_dialogs;

import javax.swing.JOptionPane;

public class KnockKnock {
public static void main(String[] args) {
	JOptionPane.showMessageDialog(null, "Knock knock?");
	JOptionPane.showMessageDialog(null, "Who is there?");
	JOptionPane.showMessageDialog(null, "Cash!");
	JOptionPane.showMessageDialog(null, "Cash who?");
	JOptionPane.showMessageDialog(null, "No thank you, I will have some peanuts.");
}
}
